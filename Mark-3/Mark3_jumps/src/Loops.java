public class Loops {
    public static void main(String[] args) {
        // print numbers from 1 to 10
        for(int i=1;i <= 5; i++) {
            System.out.println(i);
        }
        int i=1;
        while (i<=5) {
            System.out.println(i);
            i++;
        }
        int j=1;
        do {
            System.out.println(j);
            j++;
        }while(j<=5);
    }
}
