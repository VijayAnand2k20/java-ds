public class Basics {
    public static void main(String[] args) {
        if(5>1) {
            System.out.println("True");
        }
        int i=10;
        while(i>=0) {
            System.out.println(i);
            i-- ;
        }
        for(int j=5; j!=0 ; j--) {
            System.out.println(j);
        }
    }
}
